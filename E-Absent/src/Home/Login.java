    private void jLabel4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel4MouseClicked
        try {
    Connection c = Koneksi.getKoneksi();
    Statement s = c.createStatement();

    // Query untuk mencari user berdasarkan username dan password
    String sql = "SELECT * FROM user WHERE username ='" + usrname.getText()
            + "' and password='" + pass.getText() + "'";
    ResultSet r = s.executeQuery(sql);

    // Jika user ditemukan
    if (r.next()) {
        // Mendapatkan peran pengguna
        String role = r.getString("role");

        // Menampilkan pesan login berhasil
        JOptionPane.showMessageDialog(null, "Login Berhasil");
        this.dispose();  // Menutup form login

        // Cek peran pengguna
        if ("admin".equals(role)) {
            // Jika admin, buka halaman Absen untuk admin
            Absen a = new Absen();
            a.setVisible(true);
        } else if ("siswa".equals(role)) {
            // Jika siswa, buka halaman AbsenSiswa untuk siswa
            AbsenSiswa absenSiswa = new AbsenSiswa();
            absenSiswa.setVisible(true);
        }
    } else {
        // Jika username atau password salah
        JOptionPane.showMessageDialog(null, "Username atau Password Salah");
        pass.requestFocus();
    }
} catch (SQLException e) {
    System.out.println("Error: " + e.getMessage());
}

    }//GEN-LAST:event_jLabel4MouseClicked

